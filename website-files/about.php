       <h3>Criteria to donate blood</h3>
       <h3>See the eligibility of an individual to donate blood before donating. Guidelines laid down by the Ministry of Health, Government of India<br></h3>

Overall health- The donor must be fit and healthy, and should not be suffering from transmittable diseases.<br>
Age and weight- The donor must be 18–65 years old and should weigh a minimum of 50  kg.<br>
Pulse rate- Between 50 and 100 without irregularities.<br>
Hemoglobin level- A minimum of 12.5 g/dL.<br>
Blood pressure- Diastolic: 50–100 mm Hg, Systolic: 100–180 mm Hg.<br>
Body temperature- Should be normal, with an oral temperature not exceeding 37.5 °C.<br>
The time period between successive blood donations should be more than 3 months.<br>
