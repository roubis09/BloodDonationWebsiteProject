      <h1>WHY OUR PLATFORM</h1>

      India, like many countries, has a shortage of safe blood. There aren’t enough people donating blood to meet the demand of people who need it. In some cases, this shortage leads to patients and their family being responsible for finding donors to replace blood in the blood banks or hospitals. This can cause high-stress situations as people try to find blood donors on their own.

      Here comes our platform to help the people. We think we can help by finding ways to more efficiently bring blood donors and people in need together. When people have better information about the crisis of blood, they’re more willing to donate blood and it’s easier for people who need blood to find donors.

      Through our platform we are trying to make people aware of the blood crisis in India. People who want to donate blood and also the people who need blood can send information through our website. We shall reach their home as soon as possible. So that through our platform people can donate blood and get blood from their home. We will Ensure Your Donated Blood Reaches the Right Person at the Right Time.
